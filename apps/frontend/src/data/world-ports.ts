// Helper functions
export const getPortById = (id: string): Port | undefined => {
  return WORLD_PORTS.find((port) => port.id === id);
};

export const getPortsByRegion = (region: string): Port[] => {
  return WORLD_PORTS.filter((port) => port.region === region);
};

export const searchPorts = (query: string): Port[] => {
  const lowerQuery = query.toLowerCase();
  return WORLD_PORTS.filter(
    (port) =>
      port.name.toLowerCase().includes(lowerQuery) ||
      port.city.toLowerCase().includes(lowerQuery) ||
      port.country.toLowerCase().includes(lowerQuery) ||
      port.code.toLowerCase().includes(lowerQuery)
  );
};

export const getRegions = (): string[] => {
  return Array.from(new Set(WORLD_PORTS.map((port) => port.region)));
};

